#Affiche la string contenant les characteres "3 + 2 < 5 - 7"
puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?"

#Renvoie un boolean etant donne que l'equation est fausse
puts 3 + 2 < 5 - 7

#Affiche la string suivie du resultat du calcul
puts "Ça fait combien 3 + 2 ? #{3 + 2}"
puts "Ça fait combien 5 - 7 ? #{5 - 7}"

